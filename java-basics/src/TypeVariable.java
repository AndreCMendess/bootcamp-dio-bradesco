public class TiposVariaveis {
    
    byte a = 100; // Armazena valores entre -128 e 127
    short b = 32000; // Armazena valores entre -32.768 e 32.767
    int c = 100000; // Armazena valores entre -2^32 e 2^31-1
    long d = 0000000000L; // Armazena valores entre -2^63 e 2^63-1
    double f = 3.14159; // Tipo padrao para numeros decimais
    float g = 3.14f; // Ponto flutuante de 32bits
    char h = 'A'; // Armazena um unico caractere

    String i = "Hello World"; // Cadeia de caracteres , um tipo de dado que armazena texto  (objeto)

    boolean j = true; // Valor logico , pode ser true ou false
   
    final double PI = 3.141592653589793; // Final para definir uma constante que n√£o pode ser alterada
}
